### Comparison of Monte Carlo methods, Examples 2.1 and 2.3.
M1 <- rep(NA,200)
M2 <- rep(NA,200)
for (i in 1:200){
M1[i] <- meth1(1000)
M2[i] <- meth2(1000)### run each method 200 times
}
library(UsingR)
Mcomb <- c(M1,M2)
comb <- c(rep(1,200),rep(2,200))
violinplot(Mcomb~comb,col=grey(0.8),
names=c("independent MC","importance sampling"))
